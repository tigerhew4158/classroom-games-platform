function beep(type){
  if(state.sound !== 'on') return;
  try{
    const ctx = new (window.AudioContext || window.webkitAudioContext)();
    const osc = ctx.createOscillator(); const gain = ctx.createGain();
    osc.connect(gain); gain.connect(ctx.destination);
    const now = ctx.currentTime;
    const freq = type==='good'?720:type==='bad'?180:type==='end'?520:360;
    osc.frequency.setValueAtTime(freq, now);
    if(type==='good') osc.frequency.exponentialRampToValueAtTime(980, now+.12);
    if(type==='bad') osc.frequency.exponentialRampToValueAtTime(110, now+.18);
    gain.gain.setValueAtTime(.001, now); gain.gain.exponentialRampToValueAtTime(.12, now+.02); gain.gain.exponentialRampToValueAtTime(.001, now+.2);
    osc.start(now); osc.stop(now+.22);
  }catch(e){}
}
