function startGameTimer(){
  clearInterval(state.gameTimer);
  if(state.gameSeconds===0){ $('gameTimer').textContent='∞'; return; }
  $('gameTimer').textContent = formatTime(state.remaining);
  state.gameTimer = setInterval(()=>{
    if(state.ended) return;
    state.remaining--;
    $('gameTimer').textContent = formatTime(state.remaining);
    if(state.remaining<=0) endGame(t('timeUp'));
  },1000);
}
function startTurnTimer(){
  clearInterval(state.turnTimer);
  if(state.turnLimit===0){ $('turnTimer').textContent='∞'; $('turnFill').style.width='100%'; return; }
  state.turnRemaining = state.turnLimit;
  $('turnTimer').textContent = state.turnRemaining;
  $('turnFill').style.width='100%';
  state.turnTimer = setInterval(()=>{
    if(state.ended || !state.started || state.answered) return;
    state.turnRemaining--;
    $('turnTimer').textContent = state.turnRemaining;
    $('turnFill').style.width = `${Math.max(0,(state.turnRemaining/state.turnLimit)*100)}%`;
    if(state.turnRemaining<=0) handleTimeout();
  },1000);
}

function nextQuestion(advanceTeam=true){
  if(!state.started || state.ended) return;
  clearInterval(state.turnTimer);
  if(advanceTeam) state.current = (state.current+1) % state.teams.length;
  if(state.qIndex >= state.questions.length){
    state.questions = $('questionMode').value === 'random' ? shuffle([...parseBank()]) : [...parseBank()];
    state.qIndex = 0;
  }
  state.currentQuestion = state.questions[state.qIndex++];
  state.questionCount++;
  state.answered = false;
  $('flashBox').classList.remove('correctFlash','wrongFlash');
  void $('flashBox').offsetWidth;
  $('flashBox').classList.add('pulse');
  setTimeout(()=>$('flashBox').classList.remove('pulse'),600);
  $('statement').textContent = state.currentQuestion.statement;
  $('statementHint').textContent = state.answerMode === 'teacher' ? t('manualHint') : t('roundInfo');
  $('questionNo').textContent = `Q${state.questionCount}`;
  $('flashBadge').textContent = 'TRUE / FALSE';
  $('explainBox').classList.remove('show');
  $('explainBox').innerHTML = '';
  $('trueBtn').disabled = false; $('falseBtn').disabled = false;
  updateHud(); renderTeams();
  startTurnTimer();
}

function normalizeJudge(v){ return v === true ? true : false; }
function chooseAnswer(choice){
  if(!state.started){ alert(t('pleaseStart')); return; }
  if(state.ended || state.answered) return;
  const correct = normalizeJudge(choice) === state.currentQuestion.answer;
  state.answered = true;
  clearInterval(state.turnTimer);
  $('trueBtn').disabled = true; $('falseBtn').disabled = true;
  revealAnswer(false);
  const team = state.teams[state.current];
  if(correct){
    team.score += state.correctPoint; team.correct++; team.streak++;
    $('flashBox').classList.add('correctFlash');
    $('resultBox').innerHTML = `✅ <b>${escapeHtml(team.name)}</b> ${escapeHtml(fmt(t('answerRight'),{n:state.correctPoint}))}`;
    log(`✅ ${team.name}: ${fmt(t('answerRight'),{n:state.correctPoint})}`);
    beep('good');
  }else{
    team.wrong++; team.streak = 0;
    let msg = t('answerWrong');
    if(state.wrongPenalty>0){ team.score -= state.wrongPenalty; msg = fmt(t('answerWrongPenalty'),{n:state.wrongPenalty}); }
    $('flashBox').classList.add('wrongFlash');
    $('resultBox').innerHTML = `❌ <b>${escapeHtml(team.name)}</b> ${escapeHtml(msg)}`;
    log(`❌ ${team.name}: ${msg}`);
    beep('bad');
  }
  updateHud(); renderTeams();
  if(checkWin()) return;
  setTimeout(()=>{ if(!state.ended) nextQuestion(true); }, 1250);
}

function handleTimeout(){
  if(state.ended || state.answered) return;
  state.answered = true;
  clearInterval(state.turnTimer);
  $('trueBtn').disabled = true; $('falseBtn').disabled = true;
  revealAnswer(false);
  const team = state.teams[state.current];
  if(state.timeoutAction === 'wrong'){
    team.wrong++; team.streak = 0;
    if(state.wrongPenalty>0) team.score -= state.wrongPenalty;
    $('flashBox').classList.add('wrongFlash');
    const msg = state.wrongPenalty>0 ? `${t('timeoutWrongMsg')} ${fmt(t('answerWrongPenalty'),{n:state.wrongPenalty})}` : t('timeoutWrongMsg');
    $('resultBox').innerHTML = `⏱️ <b>${escapeHtml(team.name)}</b> ${escapeHtml(msg)}`;
    log(`⏱️ ${team.name}: ${msg}`);
    beep('bad');
    updateHud(); renderTeams();
    if(checkWin()) return;
  }else{
    $('resultBox').innerHTML = `⏱️ ${escapeHtml(t('timeoutPassMsg'))}`;
    log(`⏱️ ${t('timeoutPassMsg')}`);
    beep('tick');
  }
  setTimeout(()=>{ if(!state.ended) nextQuestion(true); }, 1200);
}

function revealAnswer(manual=true){
  if(!state.currentQuestion) return;
  const ansText = state.currentQuestion.answer ? t('answerTrue') : t('answerFalse');
  const exp = state.currentQuestion.explanation ? `<br><span>${escapeHtml(state.currentQuestion.explanation)}</span>` : '';
  $('explainBox').innerHTML = `<b>${escapeHtml(t('correctAnswer'))}：${escapeHtml(ansText)}</b>${exp}`;
  $('explainBox').classList.add('show');
  if(manual){
    $('resultBox').innerHTML = `👀 ${escapeHtml(t('correctAnswer'))}：<b>${escapeHtml(ansText)}</b>`;
  }
}

function checkWin(){
  if(state.targetScore>0){
    const winner = state.teams.find(tm=>tm.score >= state.targetScore);
    if(winner){ endGame(`${winner.name} ${t('targetReached')}`); return true; }
  }
  return false;
}
function endGame(reason){
  if(state.ended) return;
  state.ended = true; clearInterval(state.gameTimer); clearInterval(state.turnTimer);
  $('trueBtn').disabled = true; $('falseBtn').disabled = true;
  const sorted = [...state.teams].sort((a,b)=> b.score-a.score || b.correct-a.correct || a.wrong-b.wrong);
  $('modalTitle').textContent = t('gameOver');
  $('modalBody').innerHTML = `<h2 style="margin-top:0">${escapeHtml(reason)}</h2><h3>${escapeHtml(t('ranking'))}</h3>` + sorted.map((tm,i)=>`
    <div class="rankRow">
      <div class="rankMedal">${i===0?'🥇':i===1?'🥈':i===2?'🥉':'🏅'}</div>
      <div>${escapeHtml(tm.emoji+' '+tm.name)}<div style="font-size:13px;color:#64748b;margin-top:4px">${escapeHtml(t('corrects'))}: ${tm.correct} · ${escapeHtml(t('wrongs'))}: ${tm.wrong}</div></div>
      <div class="rankScore">${tm.score}</div>
    </div>`).join('') + `<div class="rowBtns"><button class="primary" onclick="closeModal();resetGame(false);initGame();">${escapeHtml(t('startGame'))}</button><button onclick="closeModal();showSettings();">${escapeHtml(t('showSettings'))}</button></div>`;
  $('modalBackdrop').classList.add('show');
  beep('end');
}

