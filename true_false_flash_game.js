function t(key){ return (I18N[state.lang] && I18N[state.lang][key]) || I18N.zh[key] || key; }
function fmt(str, vars={}){ return String(str).replace(/\{(\w+)\}/g,(_,k)=> vars[k] ?? ''); }
function pad(n){ return String(n).padStart(2,'0'); }
function formatTime(sec){ if(state.gameSeconds===0) return '∞'; return `${pad(Math.floor(Math.max(0,sec)/60))}:${pad(Math.max(0,sec)%60)}`; }
function shuffle(arr){ for(let i=arr.length-1;i>0;i--){ const j=Math.floor(Math.random()*(i+1)); [arr[i],arr[j]]=[arr[j],arr[i]]; } return arr; }
function escapeHtml(s){ return String(s).replace(/[&<>"']/g,c=>({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#039;'}[c])); }

