function buildBoxes(n){
  // Distribution keeps the game lively but still question-centered.
  const events=[];
  const qCount=Math.ceil(n*.42), bonus=Math.ceil(n*.18), trap=Math.ceil(n*.14), special=Math.max(0,n-qCount-bonus-trap-2);
  for(let i=0;i<qCount;i++) events.push('quiz');
  for(let i=0;i<bonus;i++) events.push('bonus');
  for(let i=0;i<trap;i++) events.push('trap');
  const specials=['steal','swap','double','empty','empty','bonus','quiz'];
  for(let i=0;i<special;i++) events.push(specials[i%specials.length]);
  while(events.length<n) events.push('quiz');
  shuffle(events);
  return events.slice(0,n).map((type,i)=>({id:i+1,type,opened:false,result:null}));
}
