function playTone(type){
  if(!state.sound) return; try{
    const ctx=new (window.AudioContext||window.webkitAudioContext)(); const o=ctx.createOscillator(); const g=ctx.createGain();
    const map={open:[220,.08],good:[660,.12],bad:[150,.18],special:[440,.08],start:[523,.12],end:[330,.2]}; const [freq,dur]=map[type]||[300,.1];
    o.frequency.value=freq; o.type=type==='bad'?'sawtooth':'sine'; g.gain.value=.08; o.connect(g); g.connect(ctx.destination); o.start(); g.gain.exponentialRampToValueAtTime(.001,ctx.currentTime+dur); o.stop(ctx.currentTime+dur+.02);
  }catch(e){}
}
