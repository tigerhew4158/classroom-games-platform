function applyEvent(box){
  const team=state.teams[state.current]; let msg='', icon=eventIcon(box.type), title=eventName(box.type);
  if(box.type==='bonus'){
    const n=[1,2,3][Math.floor(Math.random()*3)]; team.score += n; msg=t('gotPoints',{n}); playTone('good');
  } else if(box.type==='trap'){
    const n=[1,2][Math.floor(Math.random()*2)]; team.score = Math.max(0, team.score-n); msg=t('lostPoints',{n}); playTone('bad');
  } else if(box.type==='empty'){
    msg=t('nothing');
  } else if(box.type==='swap'){
    let candidates=state.teams.map((tm,i)=>({tm,i})).filter(x=>x.i!==state.current);
    candidates.sort((a,b)=>b.tm.score-a.tm.score);
    const other=candidates[0]; if(other){ const tmp=team.score; team.score=other.tm.score; other.tm.score=tmp; msg=t('swapped',{team:other.tm.name}); playTone('special'); } else msg=t('nothing');
  } else if(box.type==='steal'){
    let candidates=state.teams.map((tm,i)=>({tm,i})).filter(x=>x.i!==state.current && x.tm.score>0).sort((a,b)=>b.tm.score-a.tm.score);
    if(candidates.length){ const other=candidates[0]; const n=Math.min(1,other.tm.score); other.tm.score-=n; team.score+=n; msg=t('stole',{team:other.tm.name,n}); playTone('special'); } else { msg=t('noSteal'); }
  }
  log(`🎁 <b>${team.name}</b> ${t('openResult')} #${box.id}: ${title} — ${msg}`);
  showEventModal(title, icon, msg, ()=>{ nextTurn(); checkEnd(); });
}
