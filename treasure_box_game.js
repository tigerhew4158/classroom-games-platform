function parseCSVLine(line){
  const out=[]; let cur='', q=false;
  for(let i=0;i<line.length;i++){
    const c=line[i];
    if(c==='"' && line[i+1]==='"'){cur+='"'; i++;}
    else if(c==='"'){q=!q;}
    else if(c===',' && !q){out.push(cur.trim()); cur='';}
    else cur+=c;
  }
  out.push(cur.trim()); return out.filter(x=>x.length>0);
}
function parseQuestions(){
  const lines=$('questionBank').value.split(/\r?\n/).map(x=>x.trim()).filter(x=>x && !x.startsWith('#'));
  return lines.map(line=>{
    const p=parseCSVLine(line);
    if(p.length<2) return null;
    return {q:p[0], ans:p[1], wrong:p.slice(2), used:false};
  }).filter(Boolean);
}
